    public ResultSet tableDataBetweenDate(String dataStart, String dateEnd, String tableName) throws ClassNotFoundException {
        ResultSet rs = null;
        Connection conn = null;
        PreparedStatement psm = null;
        try {
            conn = DatabaseConnection.getConnection();
            String sql = "select * from "
                    + tableName
                    + " where date between '"
                    + dataStart
                    + "' and '"
                    + dateEnd
                    + "'";

            psm = conn.prepareStatement(sql);

            rs = psm.executeQuery();

            return rs;
        } catch (SQLException ex) {
            System.err.println("tableDataBetweenDate " + ex);
        }
        return rs;
    }

    public int columnSum(String columnName, String tableName) throws ClassNotFoundException {
        int sum = 0;
        Connection conn = null;
        PreparedStatement psm = null;
        ResultSet rs = null;
        try {

            conn = DatabaseConnection.getConnection();
            String sql = "Select SUM("
                    + columnName
                    + ") FROM "
                    + tableName
                    + " ";
            psm = conn.prepareStatement(sql);

            rs = psm.executeQuery();

            if (rs.next()) {
                sum = rs.getInt(1);
            }
        } catch (SQLException ex) {
            System.err.println("columnSum " + ex);
        } finally {
            if (conn != null || psm != null || rs != null) {
                try {
                    conn.close();
                    psm.close();
                    rs.close();
                } catch (SQLException ex) {
                    Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }

        return sum;
    }

